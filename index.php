 <div class="safer-line">
 </div>
 <h1 class="safer-heading---home">
 ESSENTIAL</h1>
 <p class="paragraph-7">The majority (75%) of freight in Australia is transported on our road network and this will continue into the future because the Australian public want their goods delivered in the most efficient and effective means possible. The public understand the essential role trucks play in the distribution of goods and services that are vital to our living standards and to the economy.</p>
