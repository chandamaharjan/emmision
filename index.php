 <h1 class="safer-heading---home">GREENER</h1>
 <p class="paragraph-7">
 Advances in truck technologies means reduced emissions making the transportation of our everyday goods and services greener and less polluting to the environment in which we all live. The enhanced productivity of today&#x27;s trucks result in less carbon dioxide being produced per tonne-kilometre of freight carried.​</p>
 <a href="/todays-trucks" class="greener-button w-button">
 Learn More</a>
